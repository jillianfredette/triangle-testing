package com.triangletesting.triangletesting;

public class Point {

    double x;
    double y;

    //point constructor given x and y coordinates
    public Point(double a, double b){
        x = a;
        y = b;
    }

    //function to get the x coordinate
    public double getX(){
        return x;
    }

    //function to get the y coordinate
    public double getY(){
        return y;
    }
}
